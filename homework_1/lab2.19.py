l_juice = (int(input("Enter amount of lemon juice (in cups):\n")))
water = (int(input("Enter amount of water (in cups):\n")))
agave = (int(input("Enter amount of agave nectar (in cups:\n")))
servings = (int(input("How many servings does this make?\n")))
print("Lemon ingredients - yields", '{:.2f}'.format(servings), "servings\n")
print('{:.2f}'.format(l_juice), "cup(s) lemon juice\n")
print('{:.2f}'.format(water), "cup(s) water\n")
print('{:.2f}'.format(agave), "cup(s) agave\n")
